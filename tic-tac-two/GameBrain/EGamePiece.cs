namespace GameBrain;

/// <summary>
/// Represents the possible game pieces in the game.
/// </summary>
public enum EGamePiece
{
    /// <summary>
    /// Represents an empty space on the game board.
    /// </summary>
    Empty,
    
    /// <summary>
    /// Represents the game piece for player X.
    /// </summary>
    X, 
    
    /// <summary>
    /// Represents the game piece for player O.
    /// </summary>
    O 
}